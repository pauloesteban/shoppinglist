    private void sololetrasnumeros(java.awt.event.KeyEvent evt){
        char c = evt.getKeyChar();
    
         if((c<'a' || c>'z') && (c<'A' || c>'Z')&& (c< '0' || c> '9') &&  (c!=(char)KeyEvent.VK_SPACE))
          {
             evt.consume();
           } 
    }
    


    
